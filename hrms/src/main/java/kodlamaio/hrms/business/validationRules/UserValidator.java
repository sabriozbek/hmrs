package kodlamaio.hrms.business.validationRules;


import java.util.regex.Matcher;
import java.util.regex.Pattern;

import kodlamaio.hrms.core.utilities.results.ErrorResult;
import kodlamaio.hrms.core.utilities.results.Result;
import kodlamaio.hrms.core.utilities.results.SuccessResult;

public class UserValidator implements BaseValidator{

	public Result isEmailValid(String email) {
		
		Pattern pattern =Pattern.compile("(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])");
		Matcher matcher=pattern.matcher(email);
		if (!matcher.matches()) {
			return new ErrorResult("Email geçersizdir.");
		}
		return new SuccessResult();
	}
	
	public Result arePasswordSame(String password,String passwordRepeat) {
		
		if (!password.equals(passwordRepeat)) {
			return new ErrorResult("Şifreniz aynı değildir.");
		}
		return new SuccessResult();
	}
}
